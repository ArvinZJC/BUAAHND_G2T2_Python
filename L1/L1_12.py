# 第2章.pptx, P31, program with expressions containing operators

x = 3
print("x =", x)

x += 3
print("After \"x += 3\", x =", x)

x -= 3
print("After \"x -= 3\", x =", x)

x *= 3
print("After \"x *= 3\", x =", x)

x /= 3
print("After \"x /= 3\", x =", x)

x **= 3
print("After \"x **= 3\", x =", x)